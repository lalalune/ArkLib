def norm_hist(h):
    return {int(k): v for k, v in h.items()}


def flats_ok(r, n):
    """Exactly two rank-2 direction buckets, both of size n, both with identically
    singular/invalid pencils, and their point sets are EXACTLY the coordinate lines."""
    fl = r["degenerate_flats"]
    if len(fl) != 2:
        return False
    want = {tuple(sorted((0, j) for j in range(n))),
            tuple(sorted((i, 0) for i in range(n)))}
    got = set()
    for f in fl:
        if f["L"] != n or f["pencil_has_valid_member"]:
            return False
        got.add(tuple(sorted(tuple(x) for x in f["point_ijs"])))
    return got == want


def assess(n, primes, results, b):
    """All load-bearing per-prime checks + the pigeonhole verdict."""
    hs = [norm_hist(r["histogram"]) for r in results]
    checks = {
        "primes_distinct": len(set(primes)) == len(primes),
        "primes_split_gt_2pow28": all(
            p > TWO28 and p % n == 1 and cen.is_prime(p) for p in primes),
        "max_is_6_everywhere": all(max(h) == 6 for h in hs),
        "histograms_bit_identical": all(h == hs[0] for h in hs),
        "recount_uncapped_everywhere": not any(r["recount_capped"] for r in results),
        "flats_are_coordinate_lines_invalid": all(flats_ok(r, n) for r in results),
        "top1_canon_identical": len({json.dumps(r["top5"][0]["incidence_canon"])
                                     for r in results if r["top5"]}) == 1,
        "M_p_equals_6_everywhere": all(r["M_p"] == 6 for r in results),
    }
    clean = all(checks.values())
    k_h = k_needed(b["B_coord_hadamard"], b["B_det_hadamard"])
    k_l1 = k_needed(b["B_coord_l1"], b["B_det_l1"])
    return {
        "n": n,
        "primes": primes,
        "k_run": len(primes),
        "k_needed_hadamard": k_h,
        "k_needed_l1": k_l1,
        "checks": checks,
        "all_checks_pass": clean,
        "histogram": hs[0] if hs else None,
        "rigorous_hadamard": clean and len(primes) >= k_h,
        "rigorous_l1": clean and len(primes) >= k_l1,
        "cap_coord_hadamard": capacity(b["B_coord_hadamard"]),
        "cap_det_hadamard": capacity(b["B_det_hadamard"]),
        "cap_coord_l1": capacity(b["B_coord_l1"]),
        "cap_det_l1": capacity(b["B_det_l1"]),
        "bounds": {k: str(v) for k, v in b.items()},
    }


