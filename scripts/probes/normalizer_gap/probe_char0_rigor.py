# ============================================================ exact bounds & pigeonhole

def bounds_for(n: int) -> dict:
    """Exact integer bounds on the case-integers of a hypothetical >= 7 plane."""
    m = n // 2
    assert m % 2 == 0, "need m even so (3*sqrt(3))^m = 3^(3m/2) is an integer"
    return {
        "m": m,
        # Hadamard route: |sigma(v_k)| <= (sqrt(3))^3, so |N(v_k)| <= 3^(3m/2);
        # |sigma(det_v)| <= 27 + 27 = 54, so |N(det_v)| <= 54^m.
        "B_coord_hadamard": 3 ** (3 * m // 2),
        "B_det_hadamard": 54 ** m,
        # cruder L1 route: |sigma(v_k)| <= ||v_k||_1 <= 6, |sigma(det_v)| <= 72.
        "B_coord_l1": 6 ** m,
        "B_det_l1": 72 ** m,
    }


def capacity(B: int) -> int:
    """Max number of DISTINCT primes > 2^28 that can divide a positive integer <= B:
    t of them have product > 2^(28t), so need 2^(28t) < B. Exact integer comparison."""
    t = 0
    while (1 << (28 * (t + 1))) < B:
        t += 1
    return t


def k_needed(B_coord: int, B_det: int) -> int:
    """Pigeonhole: invisibility at k primes assigns each to one of the three case
    integers (two bounded by B_coord, one by B_det); impossible once
    k > 2*cap(B_coord) + cap(B_det)."""
    return 2 * capacity(B_coord) + capacity(B_det) + 1


# ============================================================ self-check 1: symbolic identity
# The census formula cross_normal() must equal the minor-form generalized cross product
# v_k = (-1)^k det(3x3 minor of [[1,1,1,1],[q0..q3],[r0..r3]] dropping column k)
# as a POLYNOMIAL IDENTITY in q0..q3, r0..r3 (exact multivariate expansion, no sampling).

def _mp_add(P, Q):
    R = dict(P)
    for k, v in Q.items():
        R[k] = R.get(k, 0) + v
        if R[k] == 0:
            del R[k]
    return R


def _mp_neg(P):
    return {k: -v for k, v in P.items()}


def _mp_sub(P, Q):
    return _mp_add(P, _mp_neg(Q))


def _mp_mul(P, Q):
    R = {}
    for k1, v1 in P.items():
        for k2, v2 in Q.items():
            k = tuple(sorted(k1 + k2))
            R[k] = R.get(k, 0) + v1 * v2
            if R[k] == 0:
                del R[k]
    return R


def _det3_mp(rows):
    a, b, c = rows
    return _mp_sub(
        _mp_add(_mp_add(_mp_mul(a[0], _mp_mul(b[1], c[2])),
                        _mp_mul(a[1], _mp_mul(b[2], c[0]))),
                _mp_mul(a[2], _mp_mul(b[0], c[1]))),
        _mp_add(_mp_add(_mp_mul(a[2], _mp_mul(b[1], c[0])),
                        _mp_mul(a[1], _mp_mul(b[0], c[2]))),
                _mp_mul(a[0], _mp_mul(b[2], c[1]))))


def selfcheck_formula_identity():
    """Exact symbolic proof that the census cross_normal equals the minor-form cross
    product coordinatewise (variables q0..q3 = vars 0..3, r0..r3 = vars 4..7)."""
    V = lambda i: {(i,): 1}
    ONE = {(): 1}
    rows = [[ONE] * 4,
            [V(0), V(1), V(2), V(3)],
            [V(4), V(5), V(6), V(7)]]
    v_minor = []
    for k in range(4):
        cols = [c for c in range(4) if c != k]
        Mk = _det3_mp([[row[c] for c in cols] for row in rows])
        v_minor.append(Mk if k % 2 == 0 else _mp_neg(Mk))
    # census formula (cross_normal in probe_char0_incidence_census.py), symbolically:
    q0, q1, q2, q3 = V(0), V(1), V(2), V(3)
    r0, r1, r2, r3 = V(4), V(5), V(6), V(7)
    aq, bq, cq = _mp_sub(q1, q0), _mp_sub(q2, q0), _mp_sub(q3, q0)
    dq, eq = _mp_sub(q2, q1), _mp_sub(q3, q1)
    ar, br, cr = _mp_sub(r1, r0), _mp_sub(r2, r0), _mp_sub(r3, r0)
    dr, er = _mp_sub(r2, r1), _mp_sub(r3, r1)
    v_code = [_mp_sub(_mp_mul(dq, er), _mp_mul(eq, dr)),
              _mp_sub(_mp_mul(cq, br), _mp_mul(bq, cr)),
              _mp_sub(_mp_mul(aq, cr), _mp_mul(cq, ar)),
              _mp_sub(_mp_mul(bq, ar), _mp_mul(aq, br))]
    same = all(v_code[k] == v_minor[k] for k in range(4))
    flip = all(v_code[k] == _mp_neg(v_minor[k]) for k in range(4))
    assert same or flip, "census cross_normal != +-(minor-form cross product)"
    return "+" if same else "-"


# ============================================================ self-check 2: heights & norms
# Exact arithmetic in Z[x]/(x^m + 1); norms as exact Bareiss determinants of the
# multiplication matrix.  Validates ||v_k||_1 <= 6, ||det_v||_1 <= 72,
# |N(v_k)| <= 3^(3m/2), |N(det_v)| <= 54^m on random surface triples through P(0,0).

def _poly_mul_mod(a, b, m):
    out = [0] * m
    for i, ai in enumerate(a):
        if ai:
            for j, bj in enumerate(b):
                if bj:
                    k = i + j
                    if k < m:
                        out[k] += ai * bj
                    else:
                        out[k - m] -= ai * bj
    return out


def _poly_sub(a, b):
    return [x - y for x, y in zip(a, b)]


def _zeta_pow(e, n):
    m = n // 2
    e %= n
    c = [0] * m
    if e < m:
        c[e] = 1
    else:
        c[e - m] = -1
    return c


def _det3_poly(rows, m):
    a, b, c = rows
    t = lambda i, j, k: _poly_mul_mod(a[i], _poly_mul_mod(b[j], c[k], m), m)
    pos = [t(0, 1, 2), t(1, 2, 0), t(2, 0, 1)]
    neg = [t(2, 1, 0), t(1, 0, 2), t(0, 2, 1)]
    out = [0] * m
    for q in pos:
        out = [x + y for x, y in zip(out, q)]
    for q in neg:
        out = _poly_sub(out, q)
    return out


def _bareiss_det(M):
    M = [row[:] for row in M]
    sz = len(M)
    sign, prev = 1, 1
    for k in range(sz - 1):
        if M[k][k] == 0:
            for r in range(k + 1, sz):
                if M[r][k] != 0:
                    M[k], M[r] = M[r], M[k]
                    sign = -sign
                    break
            else:
                return 0
        for i in range(k + 1, sz):
            for j in range(k + 1, sz):
                M[i][j] = (M[i][j] * M[k][k] - M[i][k] * M[k][j]) // prev
        prev = M[k][k]
    return sign * M[sz - 1][sz - 1]


def _norm_exact(g, m):
    """N(g(zeta)) = det of multiplication-by-g on Z[x]/(x^m+1) (exact integer)."""
    M = [[0] * m for _ in range(m)]
    for j in range(m):
        for i, ci in enumerate(g):
            if ci:
                k = i + j
                if k < m:
                    M[k][j] += ci
                else:
                    M[k - m][j] -= ci
    return _bareiss_det(M)


def _cross_poly(Q, R, n):
    """Minor-form cross product of (P00, Q, R), entries in Z[x]/(x^m+1)."""
    m = n // 2
    one = _zeta_pow(0, n)
    rows = [[one] * 4, Q, R]
    v = []
    for k in range(4):
        cols = [c for c in range(4) if c != k]
        Mk = _det3_poly([[row[c] for c in cols] for row in rows], m)
        v.append(Mk if k % 2 == 0 else [-x for x in Mk])
    return v


def selfcheck_heights(samples_per_n=60, seed=4687191139):
    """Exact height/norm verification on random surface triples for n in 8..64."""
    import random
    rng = random.Random(seed)
    checked = 0
    for n in (8, 16, 32, 64):
        m = n // 2
        b = bounds_for(n)
        for _ in range(samples_per_n):
            (i1, j1), (i2, j2) = (0, 0), (0, 0)
            while (i1, j1) == (0, 0) or (i2, j2) in ((0, 0), (i1, j1)):
                i1, j1 = rng.randrange(n), rng.randrange(n)
                i2, j2 = rng.randrange(n), rng.randrange(n)
            P = lambda i, j: [_zeta_pow(i + j, n), _zeta_pow(j, n),
                              _zeta_pow(i, n), _zeta_pow(0, n)]
            v = _cross_poly(P(i1, j1), P(i2, j2), n)
            for vk in v:
                l1 = sum(abs(c) for c in vk)
                assert l1 <= 6, f"L1(v_k)={l1} > 6 at n={n}"
                if any(vk):
                    N = abs(_norm_exact(vk, m))
                    assert 0 < N <= b["B_coord_hadamard"], \
                        f"|N(v_k)|={N} > 3^(3m/2) at n={n}"
            det_v = _poly_sub(_poly_mul_mod(v[0], v[3], m),
                              _poly_mul_mod(v[1], v[2], m))
            l1 = sum(abs(c) for c in det_v)
            assert l1 <= 72, f"L1(det_v)={l1} > 72 at n={n}"
            if any(det_v):
                N = abs(_norm_exact(det_v, m))
                assert 0 < N <= b["B_det_hadamard"], \
                    f"|N(det_v)|={N} > 54^m at n={n}"
            checked += 1
    return checked


def selfcheck_reduction_commutes(seed=232):
    """phi_p(symbolic cross product) == census cross_normal of the reduced points
    (up to the global sign from selfcheck_formula_identity), spot-checked exactly."""
    import random
    rng = random.Random(seed)
    for n in (8, 32, 64):
        p = cen.split_primes(n, 1)[0]
        z = cen.order_n_element(p, n)
        for _ in range(40):
            (i1, j1), (i2, j2) = (0, 0), (0, 0)
            while (i1, j1) == (0, 0) or (i2, j2) in ((0, 0), (i1, j1)):
                i1, j1 = rng.randrange(n), rng.randrange(n)
                i2, j2 = rng.randrange(n), rng.randrange(n)
            P = lambda i, j: [_zeta_pow(i + j, n), _zeta_pow(j, n),
                              _zeta_pow(i, n), _zeta_pow(0, n)]
            v = _cross_poly(P(i1, j1), P(i2, j2), n)
            phi = lambda g: sum(c * pow(z, e, p) for e, c in enumerate(g)) % p
            v_red = tuple(phi(vk) for vk in v)
            Pp = lambda i, j: (pow(z, (i + j) % n, p), pow(z, j, p), pow(z, i, p), 1)
            w = cen.cross_normal((1, 1, 1, 1), Pp(i1, j1), Pp(i2, j2), p)
            assert v_red == w or v_red == tuple((-x) % p for x in w), \
                f"reduction does not commute at n={n} p={p} ({i1},{j1}),({i2},{j2})"
    return True


