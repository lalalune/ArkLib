def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--ns", type=int, nargs="*", default=[32, 64])
    ap.add_argument("--jobs", type=int, default=6)
    ap.add_argument("--k32", type=int, default=8)
    ap.add_argument("--k64", type=int, default=12)
    args = ap.parse_args()

    print("self-check 0: census cross-product vs nullspace ...", flush=True)
    cen.self_test()
    print("self-check 1: symbolic identity census formula == minor form ...", flush=True)
    sign = selfcheck_formula_identity()
    print(f"  identity holds (global sign {sign})", flush=True)
    print("self-check 2: exact heights/norms on random surface triples ...", flush=True)
    nchk = selfcheck_heights()
    print(f"  {nchk} triples checked (L1<=6, L1(det)<=72, |N|<=3^(3m/2), |N(det)|<=54^m)",
          flush=True)
    print("self-check 3: reduction commutes with the symbolic cross product ...", flush=True)
    selfcheck_reduction_commutes()
    print("  OK", flush=True)

    kfor = {32: args.k32, 64: args.k64, 8: 2, 16: 2}
    data = {"runs": {}}

    for n in args.ns:
        b = bounds_for(n)
        k_h = k_needed(b["B_coord_hadamard"], b["B_det_hadamard"])
        k_l1 = k_needed(b["B_coord_l1"], b["B_det_l1"])
        k_run = max(kfor.get(n, k_l1), k_l1)
        primes = cen.split_primes(n, k_run)
        print(f"== n={n}: k_needed={k_h} (Hadamard) / {k_l1} (L1); running k={k_run} "
              f"primes: {primes}", flush=True)
        t0 = time.time()
        results = {}
        with Pool(processes=min(args.jobs, k_run), maxtasksperchild=1) as pool:
            for p, r in pool.imap_unordered(_worker, [(n, p) for p in primes]):
                results[p] = r
                hmax = max(int(k) for k in r["histogram"])
                print(f"  [n={n}] p={p} done in {r['wall_sec']}s: M_p={r['M_p']} "
                      f"hist_max={hmax} planes={r['n_distinct_planes']} "
                      f"capped={r['recount_capped']}  ({len(results)}/{k_run})", flush=True)
        ordered = [results[p] for p in primes]
        a = assess(n, primes, ordered, b)
        a["wall_total_sec"] = round(time.time() - t0, 1)
        data["runs"][str(n)] = {"assessment": a, "per_prime": {str(p): results[p] for p in primes}}
        print(f"== n={n}: all_checks={a['all_checks_pass']} rigorous_hadamard="
              f"{a['rigorous_hadamard']} rigorous_l1={a['rigorous_l1']} "
              f"({a['wall_total_sec']}s)", flush=True)
        with open(JSON_RIGOR, "w") as fh:
            json.dump(data, fh, indent=1, default=str)

    # ---- fold in n = 8, 16 from the stored two-prime census (k_needed = 1, 2)
    with open(JSON_CENSUS) as fh:
        census_data = json.load(fh)
    for n in (8, 16):
        if str(n) in data["runs"]:
            continue
        stored = census_data["runs"][str(n)]
        primes = stored["primes"]
        ordered = [stored["per_prime"][str(p)] for p in primes]
        a = assess(n, primes, ordered, bounds_for(n))
        a["source"] = "stored two-prime census (results_char0_census.json)"
        data["runs"][str(n)] = {"assessment": a, "per_prime": "see results_char0_census.json"}

    # ---- cross-check: ladder histograms at the original census primes match stored
    for n in args.ns:
        stored = census_data["runs"].get(str(n))
        if not stored:
            continue
        for p in stored["primes"]:
            r_new = data["runs"][str(n)]["per_prime"].get(str(p))
            if r_new is not None:
                assert norm_hist(r_new["histogram"]) == norm_hist(
                    stored["per_prime"][str(p)]["histogram"]), \
                    f"rerun at n={n} p={p} does not reproduce the stored histogram"
        print(f"== n={n}: rerun reproduces stored two-prime histograms bit-identically",
              flush=True)

    # ---- reports
    per_n = {k: v["assessment"] for k, v in data["runs"].items()}
    bound_rows = []
    for nkey in sorted(per_n, key=int):
        nn = int(nkey)
        b = bounds_for(nn)
        a = per_n[nkey]
        bound_rows.append(
            f"| {nn} | {b['m']} | {b['B_coord_hadamard']} | "
            f"{b['B_coord_hadamard'].bit_length()} | {b['B_det_hadamard']} | "
            f"{b['B_det_hadamard'].bit_length()} | {a['cap_coord_hadamard']} | "
            f"{a['cap_det_hadamard']} | {a['k_needed_hadamard']} | "
            f"{a['k_needed_l1']} | {a['k_run']} |")
    run_rows = []
    hist_rows = []
    for nkey in sorted(per_n, key=int):
        run = data["runs"][nkey]
        if isinstance(run["per_prime"], str):
            continue
        ladder_hist = per_n[nkey]["histogram"]
        for pstr, r in run["per_prime"].items():
            run_rows.append(
                f"| {nkey} | {pstr} | {r['z']} | {r['M_p']} | "
                f"{norm_hist(r['histogram']) == ladder_hist} | {r['recount_capped']} | "
                f"{'2xL=' + nkey + ' invalid' if flats_ok(r, int(nkey)) else 'UNEXPECTED'} | "
                f"{r.get('wall_sec', '-')} |")
    both = [k for k in ("32", "64") if k in per_n]
    overall_ok = all(per_n[k]["rigorous_hadamard"] and per_n[k]["rigorous_l1"] for k in both) \
        and len(both) == 2
    overall = ("**Overall: the upper bound M(n) <= 6 is now RIGOROUS at every "
               "n in {8, 16, 32, 64}**, hence M(n) = 6 at all four sizes (lower bound: "
               "RESULTS-CHAR0-ANCHOR.md)." if overall_ok else
               "**Overall: NOT fully rigorous — see per-n verdicts above.**")
    write_md(per_n, bound_rows, run_rows, hist_rows, overall)

    # ---- rigor block into the census JSON
    census_data["rigor"] = {
        "date": "2026-06-12",
        "method": ("exact invisibility bound: a char-0 plane with >= 7 incidences fixes "
                   "three nonzero integers D_bc, D_ad <= 3^(3m/2), D_det <= 54^m (norms of "
                   "cross-product coordinates / determinant); invisibility at a clean split "
                   "prime p > 2^28 forces p to divide one of them; capacity pigeonhole gives "
                   "ladder length k(n) = 2*cap(3^(3m/2)) + cap(54^m) + 1; see "
                   "RESULTS-CHAR0-RIGOR.md"),
        "per_n": {
            nkey: {
                "k_needed_hadamard": per_n[nkey]["k_needed_hadamard"],
                "k_needed_l1": per_n[nkey]["k_needed_l1"],
                "k_run": per_n[nkey]["k_run"],
                "primes": per_n[nkey]["primes"],
                "histogram": per_n[nkey]["histogram"],
                "all_checks_pass": per_n[nkey]["all_checks_pass"],
                "upper_bound_rigorous": per_n[nkey]["rigorous_hadamard"]
                and per_n[nkey]["rigorous_l1"],
                "B_coord_hadamard": str(bounds_for(int(nkey))["B_coord_hadamard"]),
                "B_det_hadamard": str(bounds_for(int(nkey))["B_det_hadamard"]),
            } for nkey in sorted(per_n, key=int)
        },
        "all_rigorous": overall_ok and all(
            per_n[k]["rigorous_hadamard"] for k in per_n),
        "files": ["RESULTS-CHAR0-RIGOR.md", "results_char0_rigor.json"],
    }
    with open(JSON_CENSUS, "w") as fh:
        json.dump(census_data, fh, indent=1, default=str)
    with open(JSON_RIGOR, "w") as fh:
        json.dump(data, fh, indent=1, default=str)
    print(f"\nwrote {MD_PATH}\nwrote {JSON_RIGOR}\nupdated {JSON_CENSUS} (rigor block)",
          flush=True)
    print("OVERALL RIGOROUS (n=32 and n=64):", overall_ok, flush=True)


if __name__ == "__main__":
    main()
